"""  
Em Python, a identação é importante pois é a 
forma que o interpretador sabe quando começa
e termina uma bloco de comando. 

"""
#exemplo:

x = 2
y = 3

if x > y :
    print("x é maior que y")
else:
    print("y é maior que x")
    
print("fora do bloco") # percebe que quando a linha sai da identação do else ele sai do bloco também



    